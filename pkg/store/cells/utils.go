package cells

import (
	"github.com/onosproject/onos-api/go/onos/ransim/types"
)

func equalNeighbors(a, b []types.NCGI) bool {

	// If one is nil, the other must also be nil.
	if (a == nil) != (b == nil) {
		return false
	}

	if len(a) != len(b) {
		return false
	}

	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}

	return true
}
